// ── LLM Judge prompt ──────────────────────────────────────────────────────
function judgePrompt(question, expectedAnswer, actualAnswer) {
  return `Ты — эксперт-экзаменатор. Оцени ответ AI-ментора на школьный вопрос.

ВОПРОС: ${question}
ЭТАЛОННЫЙ ОТВЕТ: ${expectedAnswer}
ОТВЕТ AI: ${actualAnswer}

Шкала:
- "correct" — ответ содержит правильный ключевой факт/значение/формулу из эталона. Дополнительные ВЕРНЫЕ детали, иная формулировка, развёрнутое пояснение или форматирование НЕ снижают оценку. Если главный факт верен — это correct, даже если сказано больше эталона.
- "partial" — суть верна, но пропущен важный ОБЯЗАТЕЛЬНЫЙ признак ответа ИЛИ есть небольшая фактическая неточность в деталях.
- "wrong" — главный факт неверен/отсутствует, либо ответ является технической ошибкой (например «[ERROR ...]»).

Не штрафуй за стиль, длину или лишние верные факты — оценивай только фактическую корректность сути.
Эталон — это ОРИЕНТИР по смыслу, а не дословный шаблон.
Отвечай ТОЛЬКО одним словом: correct, partial или wrong.`;
}

// ── Main ──────────────────────────────────────────────────────────────────
async function runBenchmark() {
  console.log(`\n🎓 Mentora Benchmark — ${QUESTIONS.length} questions\n`);
  
  const results = [];
  let correct = 0, partial = 0, wrong = 0;

  for (const item of QUESTIONS) {
    process.stdout.write(`[${String(item.id).padStart(3, " ")}/100] ${item.subj}: ${item.q.slice(0, 60)}…`);
    
    // Step 1: Ask Mentora (Claude Haiku 4.5)
    let answer = "";
    try {
      const resp = await callClaude({
        model: "claude-haiku-4-5-20251001",
        max_tokens: 512,
        system: MENTORA_SYSTEM,
        messages: [{ role: "user", content: item.q }],
      });
      answer = resp.content[0].type === "text" ? resp.content[0].text : "";
    } catch (e) {
      answer = `[ERROR: ${e.message}]`;
    }

    // Step 2: Judge (Claude Sonnet 4.6)
    let grade = "wrong";
    try {
      const judgeResp = await callClaude({
        model: "claude-sonnet-4-6",
        max_tokens: 10,
        messages: [{ role: "user", content: judgePrompt(item.q, item.ans, answer) }],
      });
      const raw = judgeResp.content[0].type === "text" ? judgeResp.content[0].text.trim().toLowerCase() : "";
      if (raw.startsWith("correct")) grade = "correct";
      else if (raw.startsWith("partial")) grade = "partial";
      else grade = "wrong";
    } catch (e) {
      grade = "wrong";
    }

    if (grade === "correct") correct++;
    else if (grade === "partial") partial++;
    else wrong++;

    const score = grade === "correct" ? "✅" : grade === "partial" ? "⚠️" : "❌";
    console.log(` ${score}`);

    results.push({ ...item, answer: answer.slice(0, 300), grade });

    // Rate limit: 1 req/s to avoid overloading
    await new Promise(r => setTimeout(r, 400));
  }

  // ── Calculate score ───────────────────────────────────────────────────
  const total = QUESTIONS.length;
  const score = ((correct + partial * 0.5) / total * 100).toFixed(1);
  const strictScore = (correct / total * 100).toFixed(1);
  const passed = parseFloat(score) >= 92;

  // ── Breakdown by subject ──────────────────────────────────────────────
  const bySubject = {};
  for (const r of results) {
    if (!bySubject[r.subj]) bySubject[r.subj] = { correct: 0, partial: 0, wrong: 0, total: 0 };
    bySubject[r.subj][r.grade]++;
    bySubject[r.subj].total++;
  }

  // ── Markdown report ───────────────────────────────────────────────────
  const date = new Date().toISOString().split("T")[0];
  let md = `# Mentora Factual Accuracy Benchmark — ${date}\n\n`;
  md += `## Итог\n\n`;
  md += `| Метрика | Значение |\n|---|---|\n`;
  md += `| Всего вопросов | ${total} |\n`;
  md += `| ✅ Correct | ${correct} (${(correct/total*100).toFixed(1)}%) |\n`;
  md += `| ⚠️ Partial | ${partial} (${(partial/total*100).toFixed(1)}%) |\n`;
  md += `| ❌ Wrong | ${wrong} (${(wrong/total*100).toFixed(1)}%) |\n`;
  md += `| **Итоговый балл** (correct + 0.5×partial) | **${score}%** |\n`;
  md += `| Строгий балл (только correct) | ${strictScore}% |\n`;
  md += `| Цель | ≥92% |\n`;
  md += `| **Результат** | ${passed ? "✅ **ПРОЙДЕНО**" : "❌ **НЕ ПРОЙДЕНО**"} |\n\n`;

  md += `## По предметам\n\n`;
  md += `| Предмет | ✅ | ⚠️ | ❌ | Точность |\n|---|---|---|---|---|\n`;
  for (const [subj, s] of Object.entries(bySubject)) {
    const subjectScore = ((s.correct + s.partial * 0.5) / s.total * 100).toFixed(0);
    md += `| ${subj} | ${s.correct} | ${s.partial} | ${s.wrong} | ${subjectScore}% |\n`;
  }

  md += `\n## Детальные результаты\n\n`;
  for (const r of results) {
    const icon = r.grade === "correct" ? "✅" : r.grade === "partial" ? "⚠️" : "❌";
    md += `### ${icon} Q${r.id}. [${r.subj}] ${r.q}\n`;
    md += `**Эталон:** ${r.ans}\n\n`;
    md += `**Ответ Mentora:** ${r.answer}\n\n`;
    md += `**Оценка:** ${r.grade}\n\n---\n\n`;
  }

  writeFileSync("benchmark-report.md", md);
  writeFileSync("benchmark-results.json", JSON.stringify({ date, score, strictScore, correct, partial, wrong, total, passed, bySubject, results }, null, 2));

  console.log(`\n${"─".repeat(50)}`);
  console.log(`✅ Correct:  ${correct}  (${(correct/total*100).toFixed(1)}%)`);
  console.log(`⚠️ Partial:  ${partial}  (${(partial/total*100).toFixed(1)}%)`);
  console.log(`❌ Wrong:    ${wrong}  (${(wrong/total*100).toFixed(1)}%)`);
  console.log(`${"─".repeat(50)}`);
  console.log(`📊 Score:    ${score}% (target: ≥92%)`);
  console.log(`${passed ? "✅ PASSED" : "❌ FAILED"}`);
  console.log(`\nReport saved: benchmark-report.md`);
  
  process.exit(passed ? 0 : 1);
}

runBenchmark().catch(e => { console.error(e); process.exit(1); });
