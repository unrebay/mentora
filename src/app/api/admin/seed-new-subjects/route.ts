export async function POST(req: NextRequest) {
  const auth = req.headers.get("x-admin-token");
  if (auth !== ADMIN_TOKEN) {
    return NextResponse.json({ error: "Forbidden" }, { status: 403 });
  }

  const { searchParams } = new URL(req.url);
  const subjectFilter = searchParams.get("subject"); // optional: filter by subject
  const embedOnly = searchParams.get("embed_only") === "true"; // re-embed existing chunks

  const supabase = createClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!
  );

  const chunksToProcess = subjectFilter
    ? CHUNKS.filter((c) => c.subject === subjectFilter)
    : CHUNKS;

  if (embedOnly) {
    // Only embed chunks that exist without embeddings
    const { data: pending } = await supabase
      .from("knowledge_chunks")
      .select("id, content")
      .in("subject", ["psychology", "economics", "philosophy"])
      .is("embedding", null)
      .limit(10);

    if (!pending?.length) {
      return NextResponse.json({ message: "All chunks already embedded!", remaining: 0 });
    }

    let processed = 0;
    for (const chunk of pending) {
      try {
        const embedResp = await fetch(
          `${process.env.NEXT_PUBLIC_SUPABASE_URL}/functions/v1/embed`,
          {
            method: "POST",
            headers: {
              "Content-Type": "application/json",
              Authorization: `Bearer ${process.env.SUPABASE_SERVICE_ROLE_KEY}`,
            },
            body: JSON.stringify({ input: chunk.content }),
            signal: AbortSignal.timeout(8000),
          }
        );
        const { embedding } = await embedResp.json();
        if (embedding) {
          await supabase
            .from("knowledge_chunks")
            .update({ embedding })
            .eq("id", chunk.id);
          processed++;
        }
      } catch (e) {
        console.error("Embed error", chunk.id, e);
      }
    }
    return NextResponse.json({ processed, message: `Embedded ${processed}/${pending.length}` });
  }

  // Insert new chunks with embeddings
  let inserted = 0;
  let skipped = 0;
  const errors: string[] = [];

  for (const chunk of chunksToProcess) {
    try {
      // Check if already exists to avoid duplicates
      const { count } = await supabase
        .from("knowledge_chunks")
        .select("*", { count: "exact", head: true })
        .eq("subject", chunk.subject)
        .eq("topic", chunk.topic);

      if ((count ?? 0) > 0) {
        skipped++;
        continue;
      }

      const embedResp = await fetch(
        `${process.env.NEXT_PUBLIC_SUPABASE_URL}/functions/v1/embed`,
        {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${process.env.SUPABASE_SERVICE_ROLE_KEY}`,
          },
          body: JSON.stringify({ input: chunk.content }),
          signal: AbortSignal.timeout(8000),
        }
      );

      if (!embedResp.ok) {
        errors.push(`embed failed for ${chunk.topic}`);
        continue;
      }

      const { embedding } = await embedResp.json();

      const { error } = await supabase.from("knowledge_chunks").insert({
        subject: chunk.subject,
        topic: chunk.topic,
        content: chunk.content,
        source: chunk.source,
        embedding,
        language: "ru",
      });

      if (error) {
        errors.push(`insert error: ${error.message} (${chunk.topic})`);
      } else {
        inserted++;
      }
    } catch (e) {
      errors.push(`exception: ${String(e)} (${chunk.topic})`);
    }
  }

  return NextResponse.json({
    inserted,
    skipped,
    errors,
    total: chunksToProcess.length,
    message: `Inserted ${inserted}, skipped ${skipped} (already existed), ${errors.length} errors out of ${chunksToProcess.length} chunks.`,
  });
}
